const fs = require('fs');
const path = require('path');

const ROOT_DIR = path.resolve(__dirname, '..');
const INDEX_FILE = path.join(ROOT_DIR, 'src', 'index.ts');
const OUT_FILE = path.join(ROOT_DIR, 'docs', 'API.md');

const GROUP_TITLE = {
  './aliases-and-guards': 'Aliases & Type Guards',
  './mapped-types': 'Mapped Types',
  './utility-types': "Flow's Utility Types",
  './functional-helpers': 'Deprecated',
};

const EXPORT_BLOCK = /export\s*{([\s\S]*?)}\s*from\s*'([^']+)';/g;

function parseNames(block) {
  return block
    .split('\n')
    .map(line => line.replace(/\r/g, '').replace(/\/\/[^\r\n]*/g, '').trim())
    .join(' ')
    .split(',')
    .map(part => part.trim())
    .filter(Boolean)
    .map(part => {
      const aliasMatch = part.match(/^([^\s]+)\s+as\s+([^\s]+)$/);
      if (!aliasMatch) {
        return { exported: part, source: part };
      }
      return {
        exported: aliasMatch[2],
        source: aliasMatch[1],
      };
    });
}

function parseIndexExports(indexContent) {
  const groups = {};
  let match;
  while ((match = EXPORT_BLOCK.exec(indexContent)) !== null) {
    const block = match[1];
    const sourcePath = match[2];
    const groupName = GROUP_TITLE[sourcePath] || sourcePath;
    const names = parseNames(block);
    if (!groups[groupName]) {
      groups[groupName] = [];
    }
    groups[groupName] = groups[groupName].concat(names);
  }
  return groups;
}

function formatEntry(entry) {
  if (entry.exported === entry.source) {
    return '`' + entry.exported + '`';
  }
  return '`' + entry.exported + '` (from `' + entry.source + '`)';
}

function renderMarkdown(groups) {
  const lines = [];
  lines.push('# API Reference (Generated)');
  lines.push('');
  lines.push('This file is generated from `src/index.ts` by `npm run docs:api`.');
  lines.push('');
  lines.push('Do not edit this file manually.');
  lines.push('');

  Object.keys(groups).forEach(groupName => {
    lines.push('## ' + groupName);
    lines.push('');
    groups[groupName]
      .sort((a, b) => a.exported.localeCompare(b.exported))
      .forEach(entry => {
        lines.push('- ' + formatEntry(entry));
      });
    lines.push('');
  });

  return lines.join('\n');
}

function run() {
  const indexContent = fs.readFileSync(INDEX_FILE, 'utf8');
  const groups = parseIndexExports(indexContent);
  const markdown = renderMarkdown(groups);

  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, markdown, 'utf8');
  console.log('Generated: ' + OUT_FILE);
}

run();
